print("\n--- Arithmetic operators ---")
print("5 + 3 = {}".format(5 + 3))
print("5 * 3 = {}".format(5 * 3))
print("5 - 3 = {}".format(5 - 3))
print("5 / 3 = {}".format(5 / 3))
print("5 // 3 = {}".format(5 // 3))
print("5 % 3 = {}".format(5 % 3))
print("divmod(5, 3) = {}".format(divmod(5, 3)))

print("\n--- Assignment operators ---")
n = 10
print("n = {}".format(n))
n += 3
print("n += 3 -> n = {}".format(n))
n *= 3
print("n *= 3 -> n = {}".format(n))
n -= 3
print("n -= 3 -> n = {}".format(n))
n /= 3
print("n /= 3 -> n = {}".format(n))
n //= 3
print("n //= 3 -> n = {}".format(n))
